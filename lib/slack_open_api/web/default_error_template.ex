defmodule SlackOpenApi.Web.DefaultErrorTemplate do
  @moduledoc """
  Provides struct and types for a DefaultErrorTemplate
  """

  @type t :: %__MODULE__{ok: false}

  defstruct [:ok]

  @doc false
  @spec __fields__(atom) :: keyword
  def __fields__(type \\ :t)

  def __fields__(:t) do
    [ok: {:const, false}]
  end
end
